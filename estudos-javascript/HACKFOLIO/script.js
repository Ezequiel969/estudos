class BootSequence {
    constructor() {
        this.bootSequence = document.getElementById('bootSequence');
        this.asciiArt = document.getElementById('asciiArt');
        this.bootLines = [
            document.getElementById('bootLine1'),
            document.getElementById('bootLine2'),
            document.getElementById('bootLine3'),
            document.getElementById('bootLine4'),
            document.getElementById('bootLine5')
        ];
        this.progressBar = document.getElementById('bootProgressBar');
        this.percentage = document.getElementById('bootPercentage');

        this._shownLines = new Set();
        this._raf = null;
        
        this.init();
    }

    applyBootTranslations() {
        if (!this.bootSequence) return;

        const lang = localStorage.getItem('hackfolio-lang') || 'en';
        const dict = translations?.[lang];
        if (!dict) return;

        this.bootSequence.querySelectorAll('[data-i18n]').forEach((element) => {
            const key = element.getAttribute('data-i18n');
            const value = dict[key];
            if (typeof value === 'string') {
                element.innerHTML = value;
            }
        });
    }
    
    init() {
        this.applyBootTranslations();
        document.body.style.overflow = 'hidden';
        this.bootSequence?.classList.add('boot-start');
        this.animate();
    }
    
    animate() {
        const reducedMotion = window.matchMedia?.('(prefers-reduced-motion: reduce)')?.matches;
        const duration = reducedMotion ? Math.min(1800, CONFIG.boot.duration) : CONFIG.boot.duration;
        const thresholds = [10, 24, 38, 52, 66];
        const start = performance.now();

        const tick = (now) => {
            const elapsed = now - start;
            const progress = Math.min(100, Math.floor((elapsed / duration) * 100));

            if (this.progressBar) this.progressBar.style.width = `${progress}%`;
            if (this.percentage) this.percentage.textContent = `${progress}%`;

            thresholds.forEach((threshold, idx) => {
                if (progress >= threshold && !this._shownLines.has(idx)) {
                    this._shownLines.add(idx);
                    this.showLine(idx, idx === thresholds.length - 1);
                }
            });

            if (progress < 100) {
                this._raf = requestAnimationFrame(tick);
                return;
            }

            setTimeout(() => this.complete(), reducedMotion ? 150 : 950);
        };

        this._raf = requestAnimationFrame(tick);
    }
    
    showLine(index, isSuccess = false) {
        if (this.bootLines[index]) {
            this.bootLines[index].classList.add('active');
            if (isSuccess) {
                this.bootLines[index].classList.add('success');
            }
        }
    }
    
    complete() {
        this.bootSequence.classList.add('hidden');
        document.body.style.overflow = 'auto';

        setTimeout(() => {
            if (window.app) {
                window.app.init();
            }
        }, 300);
    }
}

