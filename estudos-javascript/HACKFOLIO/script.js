class LanguageManager {
    constructor() {
        this.currentLang = localStorage.getItem('hackfolio-lang') || 'en';
        this.langToggle = document.getElementById('langToggle');
        this.langFlag = document.getElementById('langFlag');
        this.langCode = document.getElementById('langCode');
        
        this.init();
    }
    
    init() {
        this.updateUI();
        
        if (this.langToggle) {
            this.langToggle.addEventListener('click', () => this.toggle());
        }

        if (document.readyState === 'complete') {
            this.applyTranslations();
        }
    }
    
    toggle() {
        this.currentLang = this.currentLang === 'en' ? 'pt-BR' : 'en';
        localStorage.setItem('hackfolio-lang', this.currentLang);
        this.updateUI();
        this.applyTranslations();

        if (window.typewriterInstance) {
            window.typewriterInstance.updateTexts(translations[this.currentLang]['typewriter']);
        }
    }
    
    updateUI() {
        if (this.langFlag && this.langCode) {
            if (this.currentLang === 'en') {
                this.langFlag.textContent = 'üá∫üá∏';
                this.langCode.textContent = 'EN';
            } else {
                this.langFlag.textContent = 'üáßüá∑';
                this.langCode.textContent = 'PT';
            }
        }
    }
    
    applyTranslations() {
        const t = translations[this.currentLang];

        // Update document language + meta
        document.documentElement.lang = this.currentLang === 'pt-BR' ? 'pt-BR' : 'en';

        const stripHtml = (value) => String(value).replace(/<[^>]*>/g, '');
        const setMeta = (selector, content) => {
            const node = document.querySelector(selector);
            if (node && typeof content === 'string') node.setAttribute('content', stripHtml(content));
        };

        if (typeof t['meta.title'] === 'string') {
            document.title = stripHtml(t['meta.title']);
        }
        setMeta('meta[name="description"]', t['meta.description']);
        setMeta('meta[property="og:title"]', t['meta.ogTitle']);
        setMeta('meta[property="og:description"]', t['meta.ogDescription']);

        // Translate innerHTML
        document.querySelectorAll('[data-i18n]').forEach((el) => {
            const key = el.getAttribute('data-i18n');
            const value = t[key];
            if (typeof value === 'string') {
                el.innerHTML = value;

                if (el.hasAttribute('data-text')) {
                    el.setAttribute('data-text', stripHtml(value));
                }
            }
        });

        // Translate attributes via data-i18n-* (e.g., data-i18n-placeholder, data-i18n-aria-label, data-i18n-data-tooltip)
        document.querySelectorAll('*').forEach((el) => {
            for (const attr of Array.from(el.attributes)) {
                if (!attr.name.startsWith('data-i18n-')) continue;
                if (attr.name === 'data-i18n') continue;

                const targetAttr = attr.name.replace('data-i18n-', '');
                const key = attr.value;
                const value = t[key];
                if (typeof value === 'string') {
                    el.setAttribute(targetAttr, stripHtml(value));
                }
            }
        });

        if (window.interactiveTerminalInstance?.updateLanguage) {
            window.interactiveTerminalInstance.updateLanguage();
        }
    }
    
    get(key) {
        return translations[this.currentLang][key] || key;
    }
}

