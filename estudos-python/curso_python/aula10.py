# AULA 10 - Concatenação e repetição de strings
# + junta strings (concatenação)
# * repete strings (multiplicação)

contatenacao = 'A' + 'B' + 'C'  # 'ABC'
print(contatenacao)

a_dez_vezes = 'A' * 10          # 'AAAAAAAAAA'
tres_vezes_kiel = 3 * 'Kiel'    # 'KielKielKiel'
print(a_dez_vezes)
print(tres_vezes_kiel)