# AULA 8 - Exercício prático com variáveis
# Criando variáveis de diferentes tipos e exibindo

nome = 'Ezequiel'           # str
sobrenome = 'Lima'          # str
idade = 29                  # int
ano_nascimento = 1996       # int
maior_de_idade = idade >= 18  # bool (True)
altura_metros = 1.85        # float

# Exibindo cada variável
print('Nome:', nome)
print('Sobrenome:', sobrenome)
print('Idade:', idade)
print('Ano de nascimento:', ano_nascimento)
print('É maior de idade?', maior_de_idade)
print('Altura em metros:', altura_metros)