# AULA 2 e 3 - Função print() e seus parâmetros
# sep = separador entre valores (padrão é espaço)
# end = o que aparece no final (padrão é \n - nova linha)

print(12, 34, sep=" ", end= '##')  # Separa com espaço, termina com ##
print(56, 78, sep='')              # Sem separador
print(9, 10, sep='-')              # Separa com hífen

# Aspas dentro de strings:
print("Ezequiel 'Lima'")   # Aspas simples dentro de duplas
print("Ezequiel \"Lima\"")  # Escape com \ para usar aspas duplas


